

public class ForloopClass {

	public static void main(String[] args) {
		
		System.out.println("Start Loop:");
		
		for (int i = 1; i <= 6; i++) {
			
			System.out.println("This Loop:_01 step: "+i);
			
			for (int j = 1; j <=10; j++) {
				System.out.println("Loop_02 step: "+j);
				
			}
			
		}
		
		System.out.println("\nEnd all loop.");
		
	}

}
