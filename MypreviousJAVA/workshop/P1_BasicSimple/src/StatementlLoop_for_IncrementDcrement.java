
public class StatementlLoop_for_IncrementDcrement {

	public static void main(String[] args) {
		
		int a=10;
	
		int sum=0;
		
		for (int i = 0; i <= 10; ++i) {
			sum=i+a;
			System.out.println(a);
			System.out.println(i);
			System.out.println(sum);
			System.out.println("\n\n");
		}
	
		
		for (int i = 0; i <= 10; i++) {
			sum=i+a;
			System.out.println(a);
			System.out.println(i);
			System.out.println(sum);
			System.out.println("\n\n");
		}
	
		
		
		
	}

}
