
public class Statement_do_While_loop_02 {

	public static void main(String[] args) {
		
		int column,y;
		
		column=1;
		y=1;
		
	
		
		
		do {
			
		y=1;
			
			do {
				
				System.out.print(y+" ");
				y=y+1;
				
			} while (y<=3);
			System.out.println("\n");
			
			column++;
			
		} while (column<=2);
		
		
	}

}
